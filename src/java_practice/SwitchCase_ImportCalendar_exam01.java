package java_practice;

import java.util.Calendar;
// 캘린더 기능 불러오기

public class SwitchCase_ImportCalendar_exam01 {
	public static void main(String[] args) {
		// month에는 오늘이 몇 월인지 들어 있습니다.
		
		int month = Calendar.getInstance().get(Calendar.MONTH) + 1;
		// getInstance()는 new 생성자처럼 클래스를 새로운 메모리에 할당하는 메소드.
		// new와 차이점 : 최초에 할당된 메모리를 계속 사용한다는 점(메모리 주소값 같음)에서 다름.
		
		String season = "";
		
		// switch문을 이용해서 season이 오늘은 어떤 계절인지 나타내게 만들어보세요.
		switch (month) {
		case 12:
		case 1:
		case 2:
			season = "겨울";
			break;
		case 3:
		case 4:
		case 5:
			season = "봄";
			break;
		case 6:
		case 7:
		case 8:
			season = "여름";
			break;
		case 9:
		case 10:
		case 11:
			season = "가을";
			break;
		}

		System.out.println("지금은 " + month + "월이고, " + season + "입니다.");
	}
}